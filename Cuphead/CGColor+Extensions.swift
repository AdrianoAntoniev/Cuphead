import UIKit

public extension CGColor {
    static let black = UIColor.black.cgColor
    static let clear = UIColor.clear.cgColor
    static let orange = UIColor.orange.cgColor
    static let blackWithAlphaOne = UIColor.black.withAlphaComponent(1).cgColor
}
